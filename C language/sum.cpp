#include<iostream>
using namespace std;
int main()
{
    int num1, num2;
    cout<<"Enter the value of num 1:\n"; /* "<<" is a insertion operator */
    cin>>num1; 

    cout<<"Enter the value of num 2:\n"; 
    cin>>num2; /* ">>" is a Extraction operator */

cout<<"The sum is "<<num1+num2;


    return 0;
}

  